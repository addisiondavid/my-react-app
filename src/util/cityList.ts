export function lookupCity(name: string): CityLocation | null {
  if (!name) return null;
  const key = name.trim().toLowerCase();
  if (CITY_MAP[key]) return CITY_MAP[key];
  // try a fuzzy match: startsWith or includes
  const found = Object.entries(CITY_MAP).find(
    ([k]) => k.startsWith(key) || k.includes(key)
  );
  return found ? found[1] : null;
}

export function listCities(): string[] {
  return Object.keys(CITY_MAP);
}
